namespace SCCryptoLib;

////////////////////////////////////////////////////////////////////////////////////////////////////
/// <summary>   An encrypted data transfer object. </summary>
///
/// <remarks>   Slam, 3/30/2023. </remarks>
////////////////////////////////////////////////////////////////////////////////////////////////////

public class EncryptedDto
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the encrypted text. </summary>
    ///
    /// <value> The encrypted text. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public string EncryptedText { get; set; } = string.Empty;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the iv. </summary>
    ///
    /// <value> The iv. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public string IV { get; set; } = string.Empty;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the key. </summary>
    ///
    /// <value> The key. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public string Key { get; set; } = string.Empty;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the digital signature. </summary>
    ///
    /// <value> The digital signature. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public string DigitalSignature { get; set; } = string.Empty;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Gets or sets the sender. </summary>
    ///
    /// <value> The sender. </value>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public string Sender { get; set; } = string.Empty;
}